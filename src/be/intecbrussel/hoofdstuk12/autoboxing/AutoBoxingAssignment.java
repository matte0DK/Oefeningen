package be.intecbrussel.hoofdstuk12.autoboxing;

public class AutoBoxingAssignment {
    public static void main(String[] args) {

        Integer a1 = 5;
        Integer a2 = 5;

        Integer b1 = 500;
        Integer b2 = 500;

        System.out.println(a1 == a2);
        System.out.println(a1 != a2);

        System.out.println(b1 == b2);
        System.out.println((b1 != b2) + "\n");


    }
}
