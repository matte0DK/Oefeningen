package be.intecbrussel.hoofdstuk04;

public class OpZesRelationeleOperatoren {
    public static void main(String[] args) {

        int myNumber = 8;
        int myNumber2 = -6;

        System.out.println((myNumber < myNumber2));
        System.out.println((myNumber <= myNumber2));
        System.out.println((myNumber > myNumber2));
        System.out.println((myNumber >= myNumber2));
        System.out.println((myNumber == myNumber2));
        System.out.println((myNumber != myNumber2));

    }

}
